    private double calcularImc(double peso,double altura){
      double imc = peso / (altura * altura);
        DecimalFormat decimalFormat = new DecimalFormat("#,##0.00");
        lblResposta.setText(String.valueOf(decimalFormat.format(imc)));
        
        return imc;
    }
    
    private void validarImc(double imc){
         
        System.out.println(imc);
        if (imc < 18.5) {
            lblFaxaImc.setText("Voce esta abaixo do seu peso ideal");
        } else {
            if(imc > 18.5 && imc <= 24.9){
                lblFaxaImc.setText("Voce esta no seu peso ideal");
            }else{
                if(imc >24.9 && imc <= 30){
                    lblFaxaImc.setText("voce esta com sobrepeso");
                }else{
                    lblFaxaImc.setText("voce esta na faixa de obesidade");
                }
        }
            
            }       
    }    
        
    

